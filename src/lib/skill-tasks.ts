// How many questions to present from the pool
const QUESTIONS_PER_QUIZ = 5;
// Minimum correct answers to pass (fail 3+ = lockout)
const MIN_CORRECT_TO_PASS = 3;
// Cooldown period in milliseconds (30 days)
const COOLDOWN_MS = 30 * 24 * 60 * 60 * 1000;

export function getTasksForSector(sector: string): SkillTask[] {
    const allTasks = SKILL_TASKS.filter((t) => t.sector === sector);
    // Shuffle and pick QUESTIONS_PER_QUIZ questions
    const shuffled = [...allTasks].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, Math.min(QUESTIONS_PER_QUIZ, shuffled.length));
}

export function getPassThreshold(): number {
    return MIN_CORRECT_TO_PASS;
}

export function getQuizSize(): number {
    return QUESTIONS_PER_QUIZ;
}

export function getVerifiedSectors(): Set<string> {
    if (typeof window === "undefined") return new Set();
    try {
        const raw = localStorage.getItem("pathfinder_verified_sectors");
        return raw ? new Set(JSON.parse(raw)) : new Set();
    } catch {
        return new Set();
    }
}

export function markSectorVerified(sector: string): void {
    if (typeof window === "undefined") return;
    const existing = getVerifiedSectors();
    existing.add(sector);
    localStorage.setItem("pathfinder_verified_sectors", JSON.stringify(Array.from(existing)));
}

// ─── Per-sector cooldown system ─────────────────────────────────────────────
// Fail 3+ questions in a SINGLE quiz → immediate 30-day lockout for that sector

export interface SectorCooldown {
    locked: boolean;
    unlockDate: Date | null;
}

interface CooldownRecord {
    lockedAt: string; // ISO timestamp when sector was locked
}

function getCooldownStore(): Record<string, CooldownRecord> {
    if (typeof window === "undefined") return {};
    try {
        const raw = localStorage.getItem("pathfinder_sector_cooldowns");
        return raw ? JSON.parse(raw) : {};
    } catch {
        return {};
    }
}

function saveCooldownStore(store: Record<string, CooldownRecord>): void {
    if (typeof window === "undefined") return;
    localStorage.setItem("pathfinder_sector_cooldowns", JSON.stringify(store));
}

export function getSectorCooldown(sector: string): SectorCooldown {
    const store = getCooldownStore();
    const record = store[sector];

    if (!record) {
        return { locked: false, unlockDate: null };
    }

    const lockedAt = new Date(record.lockedAt).getTime();
    const unlockTime = lockedAt + COOLDOWN_MS;
    const now = Date.now();

    // If cooldown has expired, auto-unlock
    if (now >= unlockTime) {
        delete store[sector];
        saveCooldownStore(store);
        return { locked: false, unlockDate: null };
    }

    // Still locked
    return {
        locked: true,
        unlockDate: new Date(unlockTime),
    };
}

export function lockSector(sector: string): SectorCooldown {
    const store = getCooldownStore();
    store[sector] = { lockedAt: new Date().toISOString() };
    saveCooldownStore(store);
    return getSectorCooldown(sector);
}

